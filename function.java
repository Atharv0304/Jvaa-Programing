public class function {

    public static void printSum (int a, int b) {
        int sum = a+b;
        System.out.println(sum);
        
    }
    public static void printSub (int a, int b) {
        int sum = a-b;
        System.out.println(sum);
        
    }
    public static void printMul (int a, int b) {
        int sum = a*b;
        System.out.println(sum);
        
    }
    public static void printDiv (int a, int b) {
        int sum = a/b;
        System.out.println(sum);
        
    }
    public static void printRem (int a, int b) {
        int sum = a%b;
        System.out.println(sum);
        
    }
    public static void main(String[] args) {

        printSum(33, 2);
        printSub(33, 2);
        printMul(33,2);
        printDiv(33, 2);
        printRem(33, 2);
        
        
    }
}
